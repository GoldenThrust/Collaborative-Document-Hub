import { model, Schema } from 'mongoose';

const UserSchema = new Schema({
    firstname: {
        type: String,
        required: true
    },
    lastname: {
        type: String,
        required: true
    },
    email: {
        type: String,
        unique: true,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    image: String
}, { timestamps: true })

const User = model('User', UserSchema);
export default User;
